package sample.pojo.data;

public class RequestInfo {

	private String rqDate;
	private String rqTime;
	private Integer allnum;
	private String vchkcode;
	private String querynumber;

	public String getRqDate() {
		return rqDate;
	}

	public void setRqDate(String rqDate) {
		this.rqDate = rqDate;
	}

	public String getRqTime() {
		return rqTime;
	}

	public void setRqTime(String rqTime) {
		this.rqTime = rqTime;
	}

	public Integer getAllnum() {
		return allnum;
	}

	public void setAllnum(Integer allnum) {
		this.allnum = allnum;
	}

	public String getVchkcode() {
		return vchkcode;
	}

	public void setVchkcode(String vchkcode) {
		this.vchkcode = vchkcode;
	}

	public String getQuerynumber() {
		return querynumber;
	}

	public void setQuerynumber(String querynumber) {
		this.querynumber = querynumber;
	}

}
